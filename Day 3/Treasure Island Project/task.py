print("Your mission is to find the treasure.")
chooseDirection = input("Please choose to go left or right. Enter left or right: ").lower().strip()
if chooseDirection != 'left':
    print("Fall into a hole. Game over.  ")
else:
    decisionTwo = input("Would you like to swim or wait? Type your choice: ").lower().strip()
    if decisionTwo != 'wait':
        print("Attacked by trout. Game over.")
    else:
        doorSelection = input("Red, Blue, or Yellow door? Enter red, blue, or yellow: ").lower().strip
